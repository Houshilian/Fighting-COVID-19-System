    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int selectedRow = tabProducts.getSelectedRow();

        if (selectedRow >= 0) {
            Airplane info = (Airplane) tabProducts.getValueAt(selectedRow, 0);
            this.agency.getAirlinerDir().getAirlinerList().get(index).getFleet().getAirplanes().remove(info);
            JOptionPane.showMessageDialog(null, "Airplane deleted.");
            this.populate();
        } else {
            JOptionPane.showMessageDialog(null, "Please select a row.");
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

