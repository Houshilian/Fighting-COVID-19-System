    public void populate()
    {
        DefaultTableModel dtm = (DefaultTableModel) tabProducts.getModel();
        dtm.setRowCount(0);
        for (Airplane i : agency.getAirlinerDir().getAirlinerList().get(index).getFleet().getAirplanes()) {
            Object row[] = new Object[4];
            row[0] = i;
            row[1] = i.getManufacturer();
            row[2] = i.getModelNumber();
            row[3] = i.getSeatCols()*i.getSeatRows();
            dtm.addRow(row);
        }
    }

