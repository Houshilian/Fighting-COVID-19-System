    public void populate()
    {
        DefaultTableModel dtm = (DefaultTableModel) tabProducts.getModel();
        dtm.setRowCount(0);
        for (Airliner i : agency.getAirlinerDir().getAirlinerList()) {
            Object row[] = new Object[3];
            row[0] = i;
            row[1] = i.getAddress();
            row[2] = i.getTotalFlightsPerDay();
            dtm.addRow(row);
        }
    }

